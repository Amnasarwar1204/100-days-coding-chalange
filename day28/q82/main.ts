// This function counts how many characters are in a string
function lengthOfString(str:string){
    
    return str.length;  // Returns the number of characters in the string
}

// Example: Measuring the length of a name
console.log(lengthOfString("Amna"));// Outputs: 4
// We're simply asking how long the string "Alice" is, and it tells us there are 4 characters.