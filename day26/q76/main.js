// This function adds two numbers and returns the result
function addNumber(num1, num2) {
    // Calculates the sum of num1 and num2
    return num1 + num2;
}
// Calling the function with two numbers and logging the result
console.log(addNumber(6, 9)); // Outputs 15
// Here, we ask our function to add 6 and 9, and it tells us the answer is 15.
