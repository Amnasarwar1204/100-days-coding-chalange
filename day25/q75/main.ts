// This function uses compound assignment operators for different operations
function useCompoundOperators() {
let a = 4;  // Starts with a equal to 4
console.log("Initial a:" , a);

a += 4
console.log("After addition:", a); // Shows a after addition

let b = 4; // Starts with b equal to 4
console.log("Initial b:" , b);

b -= 4
console.log("After subtraction:", b); // Shows b after subtraction

let c = 4; // Starts with c equal to 4
console.log("Initial c:" , c);

c *= 4
console.log("After multiplication:", c); // Shows c after multiplication

let d = 4; // Starts with d equal to 4
console.log("Initial d:" , d);

d /= 4
console.log("After division:", d); // Shows d after division

};

console.log(useCompoundOperators());
// We simplify arithmetic operations on x using compound assignment operators, like a shortcut.