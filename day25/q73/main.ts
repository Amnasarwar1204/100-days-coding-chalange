// This function shows how to assign and update variable values
function updateVariable(){
    let age = 29; // Initially assigns the value 29
    console.log("inital value:" ,age); // Logs the initial value
    age = 30; // Updates the value to 30
    console.log("updated value:" ,age); // Logs the updated value
}



updateVariable();
// We assign a value to a variable and then update it, showing how values can change.